def soma(n1, n2):
    '''
    Retorna a soma de 2 numeros
    -----------------------------

    Parametros
    ------------

    n1, n2: int ou float 
    Descrição: numeros a serem somados

    Retorno
    ------------
    retorna um valor int ou float 
    Descrição: retorna a soma de n1 + n2
    '''
    s = n1 + n2
    return s


x = soma(1, 2)
print(x)
